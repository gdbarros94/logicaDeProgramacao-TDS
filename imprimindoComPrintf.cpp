#include <iostream>

int main() {
    int numero;

    // Solicita ao usuário para digitar um número
    printf("Digite um número: ");
    
    // Lê o número digitado pelo usuário
    scanf("%d", &numero);

    // Imprime o número digitado pelo usuário
    printf("Você digitou: %d\n", numero);

    return 0;
}